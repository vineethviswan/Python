my_integer_var = 10
print ("Value : ", my_integer_var, " Type : ", type(my_integer_var))

my_float_var = 20.5
print ("Value : ", my_float_var, " Type : ", type(my_float_var))

my_string_var = "Hello, Python!"
print ("Value : ", my_string_var, " Type : ", type(my_string_var))

my_boolean_var = True
print ("Value : ", my_boolean_var, " Type : ", type(my_boolean_var))

my_set_var = {7, 5, 8, 9}
print ("Value : ", my_set_var, " Type : ", type(my_set_var))

my_dictinary_var = {"name": "Alice", "age": 30}
print ("Value : ", my_dictinary_var, " Type : ", type(my_dictinary_var))    

my_tuple_var = (1, 2, 3, 4)
print ("Value : ", my_tuple_var, " Type : ", type(my_tuple_var))    

my_range_var = range(5)
print ("Value : ", my_range_var, " Type : ", type(my_range_var))   

my_list_var = [22, 'Hello world', 3.14, True]
print ("Value : ", my_list_var, " Type : ", type(my_list_var))

my_none_var = None
print ("Value : ", my_none_var, " Type : ", type(my_none_var))

